ol_svg_icons.numbered_marker = (args) => {
    const number = args[0];
    const color = args[1];
    const addRing = args[2];
    const svg = `<svg width="19.9151mm" height="18.6777mm" viewBox="-0.3554 -17.6777 19.9151 18.6777" version="1.1" xmlns="http://www.w3.org/2000/svg" style="stroke-linecap:round;stroke-linejoin:round;fill:none">
                    <style>
                        .normal {
                            font-size: 5px;
                            font-weight: bold;
                            font-family: sans-serif;
                            fill: ${color};
                            stroke: #000000;
                            stroke-width: 0.1;
                        }
                    </style>
                    <g transform="scale(1,-1)">
                        <circle cx="3.3223103354652017" cy="14" r="1" style="fill:${color}; stroke:#000000; stroke-width:0.1;"/>
                        ${ringSVG(addRing)}
                    </g>
                    <text x="3.5" y="-9.5" class="normal">${number}</text>
                </svg>`;
    return {
        scale: 2, 
        src: 'data:image/svg+xml;base64,' + btoa(svg),
    };

    function ringSVG(addRing) {
        let result = "";
        
        
        if (addRing) {
            result += '<circle cx="3.3223103354652017" cy="14" r="3.0" style="stroke:#000000; stroke-width:0.6;"/>'
            result += '<circle cx="3.3223103354652017" cy="14" r="3.0" style="stroke:#009bff; stroke-width:0.5;"/>'
        }
        return result;
    }
};



